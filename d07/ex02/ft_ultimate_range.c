#include <stdlib.h>

int		ft_ultimate_range(int **range, int min, int max)
{
	int counter;

	counter = min;
	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	*range = (int *)malloc(sizeof(int) * (max - min));
	while (counter < max)
	{
		(*range)[counter - min] = counter;
		counter++;
	}
	return (counter - min);
}
