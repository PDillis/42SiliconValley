int		ft_iterative_power(int nb, int power);

int		ft_iterative_power(int nb, int power)
{
	int i;
	int count;

	i = nb;
	count = 1;
	if (power < 0)
	{
		return (0);
	}
	if (power == 0)
	{
		return (1);
	}
	while (count < power)
	{
		nb *= i;
		count++;
	}
	return (nb);
}
