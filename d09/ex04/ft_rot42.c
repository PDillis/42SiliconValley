char	*ft_rot42(char *str)
{
	int con;
	int i;

	i = 0;
	while (str[i])
	{
		con = 0;
		if ((97 <= str[i] && str[i] <= 122) || (65 <= str[i] && str[i] <= 90))
		{
			while (con < 42)
			{
				str[i]++;
				if (str[i] == 123)
					str[i] = 'a';
				if (str[i] == 91)
					str[i] = 'A';
				con++;
			}
		}
		i++;
	}
	return (str);
}
