int		ft_antidote(int i, int j, int k)
{
	if ((i <= j && j <= k) || (k <= j && j <= i))
		return (j);
	if ((j <= k && k <= i) || (i <= k && k <= j))
		return (k);
	if ((k <= i && i <= j) || (j <= i && i <= k))
		return (i);
}
